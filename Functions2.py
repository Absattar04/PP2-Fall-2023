def compute_average_imdb_score_by_category(movies, category):
    total_score = 0
    count = 0
    for movie in movies:
        if movie["category"] == category:
            total_score += movie["imdb"]
            count += 1
    if count == 0:
        return 0
    average_score = total_score / count
    return average_score

